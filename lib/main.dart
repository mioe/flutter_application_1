      date: DateTime.now(),
    ),
    Transaction(
      id: '2',
      title: 't2',
      amount: 14.30,
      date: DateTime.now(),
    ),
  ];

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: const Text('Flutter App'),
      ),
      body: Column(
        crossAxisAlignment: CrossAxisAlignment.stretch,
        children: [
          const Card(
            child: SizedBox(
              width: double.infinity,
              child: Text('Chart!'),
            ),
            elevation: 5,
          ),
          Card(
            child: Container(
              padding: EdgeInsets.all(10),
              child: Column(
